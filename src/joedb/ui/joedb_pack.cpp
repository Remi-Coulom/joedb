#include "joedb/ui/dump.h"
#include "joedb/ui/process_journal_pair.h"
#include "joedb/ui/main_wrapper.h"

namespace joedb
{
 ////////////////////////////////////////////////////////////////////////////
 static void process
 ////////////////////////////////////////////////////////////////////////////
 (
  Readonly_Journal &input,
  Writable_Journal &output,
  int64_t checkpoint
 )
 {
  pack(input, output);
 }

 ////////////////////////////////////////////////////////////////////////////
 static int pack(Arguments &arguments)
 ////////////////////////////////////////////////////////////////////////////
 {
  return process_journal_pair(arguments, joedb::process);
 }
}

/////////////////////////////////////////////////////////////////////////////
int main(int argc, char **argv)
/////////////////////////////////////////////////////////////////////////////
{
 return joedb::main_wrapper(joedb::pack, argc, argv);
}
